    DELTA = [(0, 0), (0, -1), (0, 1), (-1, 0), (1, 0)]
    INF = 10 ** 18
    MOD = 1_000_000_009
    t = 1
    answers = []
    for hh in range(t):
        N, M, a, K = (int(input()) for _ in range(4))
        remaining_people = a - K
        if remaining_people == 0:
            max_rank = 1
            min_rank = 1
        else:
            max_rank = remaining_people + 1 if remaining_people + 1 <= N else N
            min_rank = (remaining_people + M - 1) // M + 1
            if min_rank > N:
                min_rank = N
        answer = f"{max_rank} {min_rank}"
        answers.append(f"{answer}")
    print(*answers, sep="\n")
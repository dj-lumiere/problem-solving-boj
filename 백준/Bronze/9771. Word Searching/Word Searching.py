with open(0, 'rb') as f:
    tokens = iter(f.read().split())
    input = lambda: next(tokens)
    print = lambda x: os.write(1, "\n".join(x).encode())
    eprint = lambda x: os.write(2, (str(x) + "\n").encode())
    t = 1
    answers = ["" for _ in range(t)]
    for i in range(t):
        target = input().decode()
        sentences = list(map(lambda x: x.decode(), tokens))
        answers[i] = f"{sum(target in i for i in sentences)}"
    print(answers)